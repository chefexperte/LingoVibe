/**
 * Get nouns by difficulty level
 * @param {string} difficulty - 'common', 'intermediate', or 'advanced'
 * @returns {Array} Filtered nouns
 */
export function getNounsByDifficulty(difficulty) {
	if (difficulty === 'advanced') {
		return russianNouns; // Include all
	}
	
	const levels = ['common', 'intermediate', 'advanced'];
	const targetIndex = levels.indexOf(difficulty);
	
	return russianNouns.filter(noun => {
		const nounIndex = levels.indexOf(noun.difficulty);
		return nounIndex <= targetIndex;
	});
}

/**
 * Get a random noun from the list
 * @param {string} difficulty - Optional difficulty filter
 * @returns {Object} Random noun object
 */
export function getRandomNoun(difficulty = 'advanced') {
	const nouns = getNounsByDifficulty(difficulty);
	return nouns[Math.floor(Math.random() * nouns.length)];
}

/**
 * Get nouns by gender
 * @param {string} gender - 'masculine', 'feminine', or 'neuter'
 * @param {string} difficulty - Optional difficulty filter
 * @returns {Array} Filtered nouns
 */
export function getNounsByGender(gender, difficulty = 'advanced') {
	const nouns = getNounsByDifficulty(difficulty);
	return nouns.filter(noun => noun.gender === gender);
}

/**
 * Get all unique words as strings
 * @returns {Array<string>} Array of Russian words
 */
export function getAllWords() {
	return russianNouns.map(noun => noun.word);
}
